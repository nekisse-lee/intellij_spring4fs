package chap03;

public class java {     //클래스블록
    public static void main(String[] args) {   //메소드블록
        //10을 변수 age의 초기값으로 저장
        int age = 10;

        //변수 age의 값을 읽고 10을 더하는 산술 연산수행
        //연산의 결과값을 변수 result의 초기값으로 저장

        int result = age + 10;

        //변수 result 의 값을 읽고 콘솔에 출력
        System.out.println(result);
    }
}
